// Helper: get translated text
export function t(
  key: string,
  lang: Lang,
  dict: any = translations
): string {
  const parts = key.split(".");
  let node: any = dict;
  for (const p of parts) {
    if (node?.[p] === undefined) return key;
    node = node[p];
  }
  return node?.[lang] ?? node?.ru ?? key;
}

// Format TJS currency
export function formatTJS(amount: number | string): string {
  const n = Number(amount);
  return new Intl.NumberFormat("ru-TJ", {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(n) + " сом.";
}

export function formatWeight(kg: number): string {
  if (kg >= 1000) return `${(kg / 1000).toFixed(1)} т`;
  return `${kg} кг`;
}
